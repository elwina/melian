int light2rgb(double la, unsigned char *r, unsigned char *g, unsigned char *b)
{
  if (la < 390 || la > 780)
  {
    *r = 255;
    *g = 255;
    *b = 255;
    return 0;
  }
  int newla = (int)(la * 10);
  int pos = (newla - 3900) * 3;
  *r = CIE_2015_10_Degree_Standard_Observer[pos];
  *g = CIE_2015_10_Degree_Standard_Observer[pos + 1];
  *b = CIE_2015_10_Degree_Standard_Observer[pos + 2];

  return 1;
}

unsigned char calcrr(double rr, int totalWave)
{
  double t = round(rr / (double)totalWave);
  if (t > 255 || checkNANINF(rr))
  {
    return (unsigned char)255;
  }
  else
  {
    return (unsigned char)t;
  }
}
